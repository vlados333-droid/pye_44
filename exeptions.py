def read_file():
    try:
        # Запрос имени файла у пользователя
        file_name = input("Введите имя файла для чтения: ")

        # Попытка открыть файл
        with open(file_name, "r") as file:
            print("Содержимое файла:")
            print(file.read())
    except FileNotFoundError:
        # Обработка ошибки, если файл не найден
        print("Файл не найден. Проверьте правильность имени файла.")
    except PermissionError:
        # Обработка ошибки, если недостаточно прав доступа
        print("Недостаточно прав для открытия файла.")
    except Exception as e:
        # Обработка любых других ошибок
        print(f"Произошла непредвиденная ошибка: {e}")


def safe_input():
    while True:
        try:
            # Запрос числа у пользователя
            number = int(input("Введите целое число: "))
            print(f"Вы ввели число: {number}")
            break  # Если всё прошло успешно, выходим из цикла
        except ValueError:
            # Обработка ошибки ввода, если введено не число
            print("Это не целое число. Попробуйте снова.")


if __name__ == "__main__":
    # Сначала обрабатываем ввод пользователя
    safe_input()

    # Затем работаем с файлами
    read_file()
